    @FXML
    public void add() throws IOException {
        var loader = new FXMLLoader(getClass().getResource("creator-dialog-view.fxml"));
        var owner = root.getScene().getWindow();
        var modalDialog = new ModalDialog(owner, loader.load());
        CreatorDialogController controller = loader.getController();
        controller.setData(modalDialog, baseList);

        modalDialog.openDialog();
    }

    private void search(@NotNull String query) {
        if (!query.isEmpty()) {
            cachedList.clear();
            for (Todo todo : baseList) {
                if (todo.getTitle().toLowerCase().contains(query.toLowerCase()))
                    cachedList.add(todo);
            }
            todoList.setItems(FXCollections.observableArrayList(cachedList));
        }else {
            todoList.setItems(baseList);
        }
    }
    
